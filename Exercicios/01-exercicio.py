""" Primeiro exercicio de Antercessor e sucessor """
number = int(input("Digite um número\n"))
print(
    f"Antecessor do número {number} é {number -1} e o sucessor é {number +1}"
)


""" Segundo exercicio Calcular a média """
nota1 = float(input("Digite a primeira nota\n"))
nota2 = float(input("Digite a segunda nota\n"))
nota3 = float(input("Digite a terceira nota\n"))
nota4 = float(input("Digite a quarta nota\n"))


media = (nota1 + nota2 + nota3 + nota4) / 4


print(f"Média das notas é {media}")
